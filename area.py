def retangulo(lado_a,lado_b):
    #Calcula a area de um calculo_retangulo
    area = lado_a*lado_b
    return area
def triangulo(lado,altura):
    #Calcula a area de um triangulo
    area  = (lado*altura)/2
    return area
def circulo(raio):
    #Calcula a area do circulo
    area = 3.14 *(raio**2)
    return area
